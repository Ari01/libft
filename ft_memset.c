#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*scpy;

	scpy = (unsigned char*)s;
	i = 0;
	while (i < n)
	{
		scpy[i] = c;
		i++;
	}
	return (s);
}
